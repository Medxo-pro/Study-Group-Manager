    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Subject1 = jComboBox1.getSelectedItem().toString(); //Obtain the "Subject1" subject from the "jComboBox1".
        String Subject2 = jComboBox2.getSelectedItem().toString(); //Obtain the "Subject2" subject from the "jComboBox2".
        String Subject3 = jComboBox3.getSelectedItem().toString(); //Obtain the "Subject3" subject from the "jComboBox3".

        String Topic1 = KeyTopic1.getText(); //Obtain the "Topic1" from the text field "KeyTopic1".
        String Topic2 = KeyTopic2.getText(); //Obtain the "Topic2" from the text field "KeyTopic2".
        String Topic3 = KeyTopic3.getText(); //Obtain the "Topic3" from the text field "KeyTopic3".

        if (Subject2.equals("")) {
            Subject2 = "null";
        }

        if (Subject3.equals("")) {
            Subject3 = "null";
        }

        if (Topic1.equals("Thermodynamics")) {
            Topic1 = "";
        }

        if (Topic2.equals("Organic Chem")) {
            Topic2 = "";
        }

        if (Topic3.equals("For Loops")) {
            Topic3 = "";
        }

        int FromT = Integer.parseInt(FromTime.getText()); //String to Int.
        int ToT = Integer.parseInt(ToTime.getText()); //String to Int.

        String Room = Rooms.getSelectedItem().toString(); //Obtain the "Room" Room from the jComboBox "Rooms".

        //https://stackoverflow.com/questions/14169240/getting-integer-values-from-textfield
        controller.insertNewGroup(UserName, Subject1, Subject2, Subject3, Topic1, Topic2, Topic3, Room, FromT, ToT); //To Add a new StudyGroup.
        controller.updateGroupStatus(UserName, 1, UserName); //Update group status. Thanks to this the program will know that the user is already in a group.
        controller.EmailsToSendNotificationsV2(Subject1, Subject2, Subject3); //Inform certain users by email that a new StudyGroup has been created.

        //Move to the "StudyGroups4" GUI class:
        StudyGroups4 x = new StudyGroups4(UserName, Subject1, Subject2, Subject3, Topic1, Topic2, Topic3, Room, FromT, ToT);
        this.hide();
        x.setVisible(true);


    }//GEN-LAST:event_jButton1ActionPerformed

