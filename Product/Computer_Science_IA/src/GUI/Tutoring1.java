    private void ProvideHelpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProvideHelpActionPerformed
        // TODO add your handling code here:
       if (!jList1.isSelectionEmpty()){ // Checks if a group has been selected before providing an error (z[0] null)
            String[] z = jList1.getSelectedValue().split(", ");
            String UserNamerecipient = z[0];
            ArrayList<String> EmailRecipient = controller.GetTutoringemail(z[0]);
            //Move to the "Tutoring3" GUI class:
            Tutoring3 x = new Tutoring3(UserName, UserNamerecipient, EmailRecipient.toString());
            this.hide();
            x.setVisible(true);
        }
        else{
           JOptionPane.showMessageDialog(null, "You first need to select a Help demand"); //Informing the user that he/she first has to select a Tutoring demand.
        }
        
        
    }//GEN-LAST:event_ProvideHelpActionPerformed

